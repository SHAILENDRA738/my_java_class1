package LoopsClass;

public class LoopsDemo {
    public static void main(String[] args) {

        for (int i = 0; i <= 10; i++) {
            System.out.println("Hello SHA" + i);
        }
        for (int i = 10; i >= 1; i--) {
            System.out.println("Hello SHA" + i);
        }
        int j = 0;
        while (j < 5) {
            System.out.println("Hello Sha" + j);
            j++;
        }
    }
}





